public class Calculator {
    public int addition(int number01,int number02){
        return number01 + number02;
    }
    public int extraction(int number01,int number02){
        return number01  - number02;
    }
    public int multiplication(int number01,int number02){
        return number01 * number02;
    }
    public int divide(int number01,int number02){
        return number01 / number02;
    }


}
